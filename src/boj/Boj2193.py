# N=1  1 [0, 1]
# N=2  10 [1, 0]
# N=3  100 101 [1, 1]
# N=4  1000 1001 1010 [2, 1]
# N=5  10000 10001 10010 10101 10100 [3, 2]
# N=6  100000 100001 100010 100100 100101 101010 101000 101001
N = int(input())
dp = [[0, 0], [0, 1], [1, 0]] + [[0, 0] for _ in range(N - 1)]

if N >= 3:
    for i in range(3, N + 1):
        dp[i][0] = dp[i - 1][0] + dp[i - 1][1]
        dp[i][1] = dp[i - 1][0]
print(dp[N][0] + dp[N][1])
