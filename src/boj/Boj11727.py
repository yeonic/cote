# n을 1, 2의 합으로 나타내는 방법 근데 2의 종류가 2개야 -> n-1
# 0 -> 0
# 1 -> 1
# 2 -> 1+1, 2, 2'
# 3 -> 1+2, 1+2', 1+1+1, 2+1, 2'+1
# 4 -> 1+1+2, 1+1+2', 2+2, 2+2', 2'+2, 2'+2'
n = int(input())
dp = [0] * (n + 1)

if n >= 1:
    dp[1] = 1
if n >= 2:
    dp[2] = 3
if n >= 3:
    for i in range(3, n + 1):
        dp[i] = dp[i - 1] + dp[i - 2] * 2

print(dp[n] % 10007)
